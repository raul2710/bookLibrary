    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (txtSearch.getText().isEmpty()) {
                JOptionPane.showMessageDialog(
                    null, "Informe o critério de pesquisa."
                );
            } else {
                if(rdbName.isSelected()){
                    configureTable();
                    List<Publisher> lista = new PublisherDAO()
                        .searchByName(txtSearch.getText());
                    if (lista.size() > 0) {
                        DefaultTableModel m = (DefaultTableModel) tabPublisher.getModel();
                        for (Publisher publisher : lista) {
                            m.addRow(new Object[]{
                                publisher.getId(),
                                publisher.getName()
                            });
                        }
                            tabPublisher.setModel(m);
                    } else {
                        JOptionPane.showMessageDialog(
                            null, "Nenhum registro encontrado."
                        );
                    }   
                }
                
                else{
                    try {
                        configureTable();
                        Publisher publisher = new PublisherDAO()
                            .searchById(Integer.parseInt(txtSearch.getText()));
                        if (publisher != null) {
                            DefaultTableModel m = (DefaultTableModel) tabPublisher.getModel();

                            m.addRow(new Object[]{
                                publisher.getId(),
                                    publisher.getName()
                            });

                            tabPublisher.setModel(m);
                        } else {
                            JOptionPane.showMessageDialog(
                                null, "Nenhum registro encontrado."
                            );
                        }  
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(
                                null, "Digite apenas números."
                        );
                    } 
                }
            }
        }
    }//GEN-LAST:event_txtSearchKeyReleased


