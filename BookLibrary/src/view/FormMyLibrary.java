    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int i, flag = 0;
        String isbn = txtSearch.getText();
           
        if(tabBooks.getRowCount() == 0){
            lblResultSearch.setText("Nenhum livro na biblioteca");
        }
        else{
            for(i=0; i<tabBooks.getRowCount(); i++){
                if(tabBooks.getValueAt(i, 4).toString().equals(isbn)){
                    lblResultSearch.setText("Livro encontrado!!");

                    lblPublisherLabel.setVisible(true);
                    lblAuthorLabel.setVisible(true);
                    lblGenreLabel.setVisible(true);
                    lblIsbnLabel.setVisible(true);

                    lblTitle.setText(tabBooks.getValueAt(i, 0).toString());
                    lblPublisher.setText(tabBooks.getValueAt(i, 1).toString());
                    lblAuthor.setText(tabBooks.getValueAt(i, 2).toString());
                    lblGenre.setText(tabBooks.getValueAt(i, 3).toString());
                    lblIsbn.setText(tabBooks.getValueAt(i, 4).toString());
                    
                    flag = 1;
                    break;  
                }   
            }
            if(flag != 1){
                lblPublisherLabel.setVisible(false);
                lblAuthorLabel.setVisible(false);
                lblGenreLabel.setVisible(false);
                lblIsbnLabel.setVisible(false);
                
                lblTitle.setVisible(false);
                lblPublisher.setVisible(false);
                lblAuthor.setVisible(false);
                lblGenre.setVisible(false);
                lblIsbn.setVisible(false);
                
                lblResultSearch.setText("Nenhum livro encontrado");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed


