    private void configureButtonGroup() {

        ButtonGroup bg = new ButtonGroup();

        bg.add(rdbId);
        bg.add(rdbName);

        rdbId.setSelected(true);
    }
    
    private void fillTable(List<Publisher> publisherList) {
        
        if (publisherList.size() > 0) {
            DefaultTableModel m = (DefaultTableModel) tabPublisher.getModel();
            m.setNumRows(0);
            for (Publisher g : publisherList) {
                m.addRow(new Object[]{
                    g.getId(),
                    g.getName()
                });
            }
            tabPublisher.setModel(m);
        } else {
            JOptionPane.showMessageDialog(
                    null, "Nenhum registro encontrado."
            );
        }
    }

    private void configureTable() {
        DefaultTableModel m = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        m.addColumn("Id");
        m.addColumn("Nome");

        m.setNumRows(0);
        tabPublisher.setModel(m);
    }
    
    private void updateTablePublisher(){
        List<Publisher> publisherList = null;
        
        if(rdbName.isSelected())
            publisherList = new PublisherDAO().listById();
        else 
            publisherList = new PublisherDAO().listById();
        
        fillTable(publisherList);
    }
