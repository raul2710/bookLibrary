    private void configureButtonGroup() {

        ButtonGroup bg = new ButtonGroup();

        bg.add(rdbTitle);
        bg.add(rdbPublisher);
        bg.add(rdbAuthor);
        bg.add(rdbGenre);
        bg.add(rdbIsbn);
        rdbTitle.setSelected(true);
    }

    private void fillTable(String search, String where) {

        List<BookInformation> listBookInfo = new BookDAO().listBookInformation(where, search);

        if (listBookInfo.size() > 0) {
            DefaultTableModel m = (DefaultTableModel) tabBooks.getModel();
            for (BookInformation bInfo : listBookInfo) {
                m.addRow(new Object[]{
                    bInfo.getId(),
                    bInfo.getIsbn(),
                    bInfo.getTitle(),
                    bInfo.getGenre(),
                    bInfo.getAuthor(),
                    bInfo.getPublisher(),
                    bInfo.getEdition()
                });
            }
            tabBooks.setModel(m);
        } else {
            JOptionPane.showMessageDialog(
                    null, "Nenhum registro encontrado."
            );
        }
    }

    private void configureTable() {
        DefaultTableModel m = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        m.addColumn("Id");
        m.addColumn("Isbn");
        m.addColumn("Título");
        m.addColumn("Gênero");
        m.addColumn("Autor");
        m.addColumn("Editora");
        m.addColumn("Edição");

        m.setNumRows(0);
        tabBooks.setModel(m);
    }

    private void initTableWithoutRow() {
        DefaultTableModel m = (DefaultTableModel) tabBooks.getModel();
        m.setNumRows(0);
        tabBooks.setModel(m);
    }

    public void openForm(JInternalFrame f) {

        Dimension d = getParent().getSize();
        getParent().add(f);
        f.setLocation(
                (d.width - f.getWidth()) / 2,
                (d.height - f.getHeight()) / 2
        );
        f.setVisible(true);

        this.dispose();
    }
