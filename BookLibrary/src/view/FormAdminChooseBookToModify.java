    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(tabBooks.getSelectedRow() >= 0){
            if(JOptionPane.showConfirmDialog(null, "Deseja mesmo excluir o livro selecionado?", "Continuar?", JOptionPane.YES_NO_OPTION) == 0)
            {
                this.book = MainMenu.bookLibrary.get(tabBooks.getSelectedRow());

                MainMenu.bookLibrary.remove(this.book);
                fillTable(MainMenu.bookLibrary); 
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Selecione algum livro!!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

