    private void tabPublisherKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tabPublisherKeyReleased
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (txtSearch.getText().isEmpty()) {
                JOptionPane.showMessageDialog(
                    null, "Informe o critério de pesquisa."
                );
            } else {
                configureTable();
                // PESQUISAR POR NOME
                List<Publisher> lista = new PublisherDAO().searchByName(txtSearch.getText());
                if (lista.size() > 0) {
                    DefaultTableModel m = (DefaultTableModel) tabPublisher.getModel();
                    for (Publisher espec : lista) {
                        m.addRow(new Object[]{
                            espec.getId(),
                            espec.getName()
                        });
                    }
                    tabPublisher.setModel(m);
                } else {
                    JOptionPane.showMessageDialog(
                        null, "Nenhum registro encontrado."
                    );
                }
            }
        }
    }//GEN-LAST:event_tabPublisherKeyReleased

