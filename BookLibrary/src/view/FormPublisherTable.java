    private void tabPublisherMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabPublisherMouseClicked
        //Double click
        if (evt.getClickCount() == 2) {
            //Descobrir a linha selecionada
            int linha = tabPublisher.getSelectedRow();

            //Recuperar o ID selecionado
            int id = Integer.parseInt(
                tabPublisher.getValueAt(linha, 0).toString()
            );

            //Abrir o FormCadastro
            FormRegisterPublisher f = new FormRegisterPublisher(id);
            this.getDesktopPane().add(f);
            f.setVisible(true);

            //Fechar o FormPesquisa
            //this.dispose();
        }
    }//GEN-LAST:event_tabPublisherMouseClicked

